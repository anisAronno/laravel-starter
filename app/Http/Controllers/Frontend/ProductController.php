    public function index()
    {
        $products = Product::with(['media', 'category'])
            ->when(request()->filled('search'), function ($query) {
                return $query->where('title', 'LIKE', '%' . request()->search . '%');
            })
            ->paginate(15)
            ->withQueryString();

